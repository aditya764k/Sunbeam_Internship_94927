let n1
// number
n1 = 10 
console.log(`n1 - ${n1} , typeof(n1) - ${typeof(n1)}`)

//String
n1 = "aditya"
console.log(`n1 - ${n1} , typeof(n1) - ${typeof(n1)}`)

//Boolean
n1 = true
console.log(`n1 - ${n1} , typeof(n1) - ${typeof(n1)}`)

//Undefined
n1 = undefined
console.log(`n1 - ${n1} , typeof(n1) - ${typeof(n1)}`)

// null 
n1 = null
console.log(`n1 - ${n1} , typeof(n1) - ${typeof(n1)}`)

// object
n1 = new Object()
n1.value = 10
n1.type = "number"
console.log(n1)
console.log(`typeof(n1) - ${typeof(n1)}`)

//array
n1 = [1,2,3,4]
console.log(`n1 - ${n1} , typeof(n1) - ${typeof(n1)}`)