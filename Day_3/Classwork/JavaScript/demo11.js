function add(n1,n2){
    return n1 + n2
}
function sub(n1,n2){
    return n1 - n2
}

function mul(n1,n2){
    return n1*n2
}
function div(n1,n2){
    return n1/n2
}

const res1 = add(21,30)
console.log(`Result - ${res1}`)

const res2 = sub(29,19)
console.log(`result - ${res2}`)

const res3 = mul(2,4)
console.log(`result - ${res3}`)

const res4 = div(10,2)
console.log(`result - ${res4}`)
